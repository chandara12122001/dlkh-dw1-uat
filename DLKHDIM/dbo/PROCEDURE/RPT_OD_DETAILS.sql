CUST_NO,CUST_AC_NO,ACCOUNT_CLASS,CCY,DORMANT,RECORD_STAT,ACCOUNT_TYPE,FULLNAME,RM_NAME,MAX_DATE

SELECT O.*,SUM(C.Num_Trn) Num_TrnD,
SUM(C.Balance) BalanceD,R.Order_Char   FROM #Overview1 O
INNER JOIN DIM_ORDER R ON R.ID=O.NUM_MONTH 
LEFT JOIN #turnD C ON O.Year=C.YEAR and o.Month=c.Month
group by o.Year,o.Month,Average_BalanceLCY,Average_BalanceFCY,Authorized_Overdraft,DayOD,o.Num_TrnC,o.BalanceC,NUM_MONTH,Order_Char,DayOVER,
CUST_NO,CUST_AC_NO,ACCOUNT_CLASS,CCY,DORMANT,RECORD_STAT,ACCOUNT_TYPE,FULLNAME,RM_NAME,MAX_DATE
