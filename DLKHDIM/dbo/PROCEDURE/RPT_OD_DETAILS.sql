INNER JOIN BBCDW..DIM_CUST_ACCOUNT AC ON BAL.ACCOUNT_KEY=AC.ACCOUNT_KEY and DATE_KEY between CONVERT(VARCHAR,@FROM_DATE,112) and CONVERT(VARCHAR,@TO_DATE,112)
INNER JOIN BBCDW..DIM_CUSTOMER CUS ON CUS.CUSTOMER_KEY = BAL.CUSTOMER_KEY and DATE_KEY between CONVERT(VARCHAR,@FROM_DATE,112) and CONVERT(VARCHAR,@TO_DATE,112)
LEFT JOIN BBCDW..DIM_RELATION_MANAGER RM ON RM.RM_ID=CUS.RM_ID AND RM.BRANCH_CODE=1
left JOIN BBCDW..FACT_CUST_ACCOUNT_LINKAGES LINK ON LINK.ACCOUNT_KEY=BAL.ACCOUNT_KEY AND  LINK.DATE_KEY between CONVERT(VARCHAR,@FROM_DATE,112) and CONVERT(VARCHAR,@TO_DATE,112)
left JOIN BBCDW..FACT_DAILY_FACILITY FAC ON CONCAT(FAC.LINE_CODE,FAC.LINE_SERIAL)=CONCAT(LINK.LINE_CODE,LINK.LINE_SERIAL) AND FAC.DATE_KEY between CONVERT(VARCHAR,@FROM_DATE,112) and CONVERT(VARCHAR,@TO_DATE,112)
INNER JOIN BBCDW..DIM_ACCOUNT_CLASS CL ON CL.ACCOUNT_CLASS=AC.ACCOUNT_CLASS
where CUST_NO=@cust_no
