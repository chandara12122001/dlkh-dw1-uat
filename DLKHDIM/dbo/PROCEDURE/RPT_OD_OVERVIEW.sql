begin

INSERT INTO #AVG
SELECT DATE_KEY+1,CUSTOMER_NO,LCY_BALANCE,FCY_BALANCE,APPROVE_AMOUNT FROM #AVG WHERE DATEADD(day,1,CAST(CONVERT(VARCHAR,(DATE_KEY),112) AS DATE)) IN (select startdate from #tmp1 where ID=@countmin and startdate not in ('2017-12-30'))		

