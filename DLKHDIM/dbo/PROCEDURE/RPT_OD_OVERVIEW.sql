select CAST(CONVERT(VARCHAR,(DATE_KEY),112) AS DATE) DATE_KEY,CUSTOMER_NO,SUM(LCY_BALANCE)LCY_BALANCE,SUM(FCY_BALANCE)FCY_BALANCE,APPROVE_AMOUNT INTO #AVGG from #AVG
GROUP BY DATE_KEY,CUSTOMER_NO,APPROVE_AMOUNT order by DATE_KEY

SELECT DATE_KEY,APPROVE_AMOUNT,(select AVG(LCY_BALANCE) FROM #AVGG) LCY_AVG,(select AVG(FCY_BALANCE) FROM #AVGG) FCY_AVG,
(SELECT COUNT(*) FROM #AVGG WHERE LCY_BALANCE<0) OD,(SELECT COUNT(*) FROM #AVGG WHERE (LCY_BALANCE*-1)>APPROVE_AMOUNT) ODOVER,(SELECT MIN(LCY_BALANCE) FROM #AVGG) MIN_BAL,(SELECT MAX(LCY_BALANCE) FROM #AVGG) MAX_BAL INTO #AVG2 FROM #AVGG

