SELECT YEAR(@FROM_DATE),DATEPART(M,@FROM_DATE),DATENAME(MONTH,@FROM_DATE),LCY_AVG,APPROVE_AMOUNT*-1,
OD,ODOVER,MIN_BAL,MAX_BAL FROM #AVG2 GROUP BY LCY_AVG,APPROVE_AMOUNT,OD,MIN_BAL,MAX_BAL,ODOVER

insert into #turnC
select YEAR(@FROM_DATE),DATENAME(m, @FROM_DATE),COUNT(TRN_REF_NO),SUM(LCY_AMOUNT),DRCR_IND,AC_NO from BBCSTAGE..SGTB_AC_ENTRIES where TRN_DT between @FROM_DATE and @TO_DATE and RELATED_CUSTOMER=@cust_no and EVENT='INIT' and CUST_GL='A' and DRCR_IND='C'
group by DRCR_IND,AC_NO
insert into #turnD
select YEAR(@FROM_DATE),DATENAME(m, @FROM_DATE),COUNT(TRN_REF_NO),SUM(LCY_AMOUNT),DRCR_IND,AC_NO from BBCSTAGE..SGTB_AC_ENTRIES where TRN_DT between @FROM_DATE and @TO_DATE and RELATED_CUSTOMER=@cust_no and EVENT='INIT' and CUST_GL='A' and DRCR_IND='D'
group by DRCR_IND,AC_NO

SET @FROM_DATE=DATEADD(MONTH,1,@FROM_DATE)
	END
SET @FROM_DATE=DATEADD(YEAR,1,@FROM_DATE)
END

SELECT O.*,SUM(C.Num_Trn) Num_TrnC,
SUM(C.Balance) BalanceC into #Overview1  FROM #Overview O 
LEFT JOIN #turnC C ON O.Year=C.YEAR and o.Month=c.Month
group by o.Year,o.Month,Average_Balance,Authorized_Overdraft,DayOD,NUM_MONTH,MIN_BAL,MAX_BAL,DayOVER

SELECT O.*,SUM(C.Num_Trn) Num_TrnD,
SUM(C.Balance) BalanceD,R.Order_Char,MAX_BAL-MIN_BAL VARI   FROM #Overview1 O
INNER JOIN DIM_ORDER R ON R.ID=O.NUM_MONTH 
LEFT JOIN #turnD C ON O.Year=C.YEAR and o.Month=c.Month
group by o.Year,o.Month,Average_Balance,Authorized_Overdraft,DayOD,o.Num_TrnC,o.BalanceC,NUM_MONTH,Order_Char,MIN_BAL,MAX_BAL,DayOVER
ORDER BY Order_Char
END
