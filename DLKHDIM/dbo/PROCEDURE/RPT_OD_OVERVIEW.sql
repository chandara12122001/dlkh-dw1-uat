DECLARE @COUNT INT=1
DECLARE @BAL INT=datediff(day,@FROM_DATE,(select CAST(CONVERT(VARCHAR,min(DATE_KEY),112) AS DATE) from #AVG))
DECLARE @MIND INT=(SELECT MIN(DATE_KEY) FROM #AVG)

WHILE @COUNT<=@BAL
BEGIN
insert into #AVG
SELECT @MIND-@COUNT,CUSTOMER_NO,
 LCY_CUR_BAL  LCY_BALANCE,
 ACY_CUR_BAL  FCY_BALANCE,(SELECT MAX(APPROVE_AMOUNT) FROM #AVG) APPROVE_AMOUNT
  FROM BBCDW..FACT_DAILY_AC_BAL BAL 
INNER JOIN BBCDW..DIM_CUSTOMER AC ON BAL.CUSTOMER_KEY=AC.CUSTOMER_KEY and DATE_KEY=@SUB_DATE
left JOIN BBCDW..FACT_CUST_ACCOUNT_LINKAGES LINK ON LINK.ACCOUNT_KEY=BAL.ACCOUNT_KEY AND  LINK.DATE_KEY=@SUB_DATE
left JOIN BBCDW..FACT_DAILY_FACILITY FAC ON CONCAT(FAC.LINE_CODE,FAC.LINE_SERIAL)=CONCAT(LINK.LINE_CODE,LINK.LINE_SERIAL) AND FAC.DATE_KEY=@SUB_DATE
where CUSTOMER_NO=@cust_no
GROUP BY CUSTOMER_NO,LCY_CUR_BAL,ACY_CUR_BAL
SET @COUNT = @COUNT+1
END
end

