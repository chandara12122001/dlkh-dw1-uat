declare @cust_no int=@CUST_NO1
declare @FROM_DATE date=@FROM_DATE1
declare @TO_DATE date=@TO_DATE1
declare @END DATE=@TO_DATE
IF OBJECT_ID('tempdb..#Overview') IS NOT NULL
  DROP TABLE #Overview
IF OBJECT_ID('tempdb..#Overview1') IS NOT NULL
  DROP TABLE #Overview1
IF OBJECT_ID('tempdb..#turnC') IS NOT NULL
  DROP TABLE #turnC
IF OBJECT_ID('tempdb..#turnD') IS NOT NULL
  DROP TABLE #turnD
create table #Overview(Year int,NUM_MONTH INT,Month varchar(max),Average_Balance money,Authorized_Overdraft money,DayOD int,DayOVER int,MIN_BAL MONEY,MAX_BAL MONEY)
create table #turnC(Year int, Month VARCHAR(500),Num_Trn int, Balance money, DRCR varchar(100),Account varchar(500))
create table #turnD(Year int, Month VARCHAR(500),Num_Trn int, Balance money, DRCR varchar(100),Account varchar(500))
while YEAR(@FROM_DATE)<=YEAR(@END)
begin
	while @FROM_DATE<=@END
	begin
set @FROM_DATE =(select dateadd(d,-1,dateadd(mm,datediff(m,0,@FROM_DATE),1 )))
set @TO_DATE = (select dateadd(d,-1,dateadd(mm,datediff(m,0,@FROM_DATE)+1,0)))
IF OBJECT_ID('tempdb..#AVG') IS NOT NULL
  DROP TABLE #AVG
IF OBJECT_ID('tempdb..#AVGG') IS NOT NULL
  DROP TABLE #AVGG
