create table #tmp1 (month int,startdate date)
select * into #tmp from BBCDW..DIM_HOLIDAY where BRANCH_CODE='001' and YEAR = (YEAR(@FROM_DATE)) and MONTH=DATEPART(m, @FROM_DATE)

declare @min_month int=1

while @min_month<=12
begin
declare @max int = (select LEN(holiday_list) from #tmp where branch_code='001' and month=@min_month)
declare @min int=1
while @min<=@max
begin
insert into #tmp1
select @min_month,(select 
case when SUBSTRING(holiday_list,@min,1)='W' then null else
CONVERT(date,cast(CONCAT(Year,RIGHT('00'+ cast(MONTH as varchar),2),right('00'+cast(@min as varchar),2)) as varchar),112) end from #tmp where MONTH=@min_month and branch_code='001')

set @min=@min+1
end
set @min_month=@min_month+1
end

delete from #tmp1 where startdate is null
alter table #tmp1
add ID int identity(1,1)

SELECT bal.DATE_KEY,CUSTOMER_NO,
 LCY_CUR_BAL  LCY_BALANCE,
 ACY_CUR_BAL  FCY_BALANCE,max(fac.COLLATERAL_CONTRIBUTION) APPROVE_AMOUNT
 into #AVG FROM BBCDW..FACT_DAILY_AC_BAL BAL 
INNER JOIN BBCDW..DIM_CUSTOMER AC ON BAL.CUSTOMER_KEY=AC.CUSTOMER_KEY and DATE_KEY between CONVERT(VARCHAR,@FROM_DATE,112) and CONVERT(VARCHAR,@TO_DATE,112)
left JOIN BBCDW..FACT_CUST_ACCOUNT_LINKAGES LINK ON LINK.ACCOUNT_KEY=BAL.ACCOUNT_KEY AND  LINK.DATE_KEY between CONVERT(VARCHAR,@FROM_DATE,112) and CONVERT(VARCHAR,@TO_DATE,112)
left JOIN BBCDW..FACT_DAILY_FACILITY FAC ON CONCAT(FAC.LINE_CODE,FAC.LINE_SERIAL)=CONCAT(LINK.LINE_CODE,LINK.LINE_SERIAL) AND FAC.DATE_KEY between CONVERT(VARCHAR,@FROM_DATE,112) and CONVERT(VARCHAR,@TO_DATE,112)
where CUSTOMER_NO=@cust_no
GROUP BY CUSTOMER_NO,LCY_CUR_BAL,ACY_CUR_BAL,bal.DATE_KEY 

declare @countmin int=1
