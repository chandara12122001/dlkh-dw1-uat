 select * INTO #EDIT from openquery(UAT7,'select * from KCAPXUT1.tedit where ETBL_LANG_CD=''E'' and CO_ID=''CP''')
SELECT * INTO #POLW FROM OPENQUERY(UAT7,'SELECT * FROM KCAPXUT1.TPOLW')
SELECT * INTO #POLC FROM OPENQUERY(UAT7,'SELECT * FROM KCAPXUT1.TPOLC')
