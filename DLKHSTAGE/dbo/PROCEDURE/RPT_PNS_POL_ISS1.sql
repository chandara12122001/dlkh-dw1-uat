  DROP TABLE #AGENT
  IF OBJECT_ID('tempdb..#DISPATCH') IS NOT NULL
  DROP TABLE #DISPATCH
    IF OBJECT_ID('tempdb..#ICS') IS NOT NULL
  DROP TABLE #ICS

  SELECT [policy_id],(DATEDIFF(DAY, dis.dispatched_date, dis.acknowledgement_date)) nb_duration,
      [created_user],dispatched_date,acknowledgement_date,welcome_date
	  into #DISPATCH
  FROM [PNSDLIKH].[dbo].[ingenium_policy_dispatches] dis

  select * INTO #AGENT from DLKHFACT..FACT_AGENT where DATE_KEY = (SELECT MAX(DATE_KEY) FROM DLKHFACT..FACT_AGENT)
  SELECT * INTO #CLI FROM OPENQUERY(UAT7,'SELECT * FROM KCAPXUT1.TCLI')
  SELECT * INTO #TCLIC FROM OPENQUERY(UAT7,'SELECT * FROM KCAPXUT1.TCLIC')
SELECT * INTO #TCLIA FROM OPENQUERY(UAT7,'SELECT * FROM KCAPXUT1.TCLIA')
