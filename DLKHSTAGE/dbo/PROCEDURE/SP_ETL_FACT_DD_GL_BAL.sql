CREATE PROCEDURE [dbo].[SP_ETL_FACT_DD_GL_BAL]
	@BRANCH_DATE DATE,@IS_RE_PROC BIT=0
AS
BEGIN
	IF @IS_RE_PROC=1
	BEGIN
		DELETE FROM  BBCDW..FACT_DAILY_GL_BAL WHERE DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112);
	END
