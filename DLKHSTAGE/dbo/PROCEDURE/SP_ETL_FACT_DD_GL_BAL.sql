DECLARE @PARA_DATE DATE;
DECLARE @CHECK BIT=1;
select @PARA_DATE=MAX(BGK_DATE)
from BBCDW..FACT_DAILY_GL_HISTORY where  BGK_DATE<@BRANCH_DATE 

select BRANCH_CODE,GL_CODE,CCY_CODE,F.FIN_YEAR,PERIOD_CODE,PARENT_GL,LEAF,CATEGORY,
ISNULL(DR_MOV,0) DR_MOV,ISNULL(CR_MOV,0)CR_MOV,ISNULL(DR_BAL,0)DR_BAL,ISNULL(CR_BAL,0)CR_BAL,
ISNULL(DR_MOV_LCY,0)DR_MOV_LCY,ISNULL(CR_MOV_LCY,0)CR_MOV_LCY,ISNULL(DR_BAL_LCY,0)DR_BAL_LCY,ISNULL(CR_BAL_LCY,0) CR_BAL_LCY   INTO #GL
from BBCDW..FACT_DAILY_GL_HISTORY H INNER JOIN  BBCDW..DIM_FIN_PERIOD F ON H.FIN_YEAR=F.FIN_YEAR AND H.PERIOD_CODE=F.FIN_MONTH  where  BGK_DATE=@PARA_DATE order by BGK_DATE

select BRANCH_CODE,GL_CODE,CCY_CODE,F.FIN_YEAR,PERIOD_CODE,CUST_AC_NO,
ISNULL(DR_MOV,0) DR_MOV,ISNULL(CR_MOV,0)CR_MOV,ISNULL(DR_BAL,0)DR_BAL,ISNULL(CR_BAL,0)CR_BAL,
ISNULL(DR_MOV_LCY,0)DR_MOV_LCY,ISNULL(CR_MOV_LCY,0)CR_MOV_LCY,ISNULL(DR_BAL_LCY,0)DR_BAL_LCY,ISNULL(CR_BAL_LCY,0) CR_BAL_LCY   INTO #BR
from BBCDW..FACT_GL_ACC_BREAKUP H INNER JOIN  BBCDW..DIM_FIN_PERIOD F ON H.FIN_YEAR=F.FIN_YEAR AND H.PERIOD_CODE=F.FIN_MONTH  where  BGK_DATE=@PARA_DATE order by BGK_DATE

select 
H.BGK_DATE,H.BRANCH_CODE,H.GL_CODE,H.CCY_CODE,H.FIN_YEAR,H.PERIOD_CODE,H.PARENT_GL,H.LEAF,H.CATEGORY,
T.DR_BAL OPN_DR,
T.CR_BAL OPN_CR,
T.DR_BAL_LCY OPN_DR_LCY,
T.CR_BAL_LCY OPN_CR_LCY,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.DR_MOV>=T.DR_MOV AND H.CR_MOV>=T.CR_MOV) THEN  H.DR_MOV - isnull(T.DR_MOV,0)
     WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.DR_MOV<T.DR_MOV OR H.CR_MOV<T.CR_MOV) THEN  H.DR_MOV  ELSE H.DR_MOV END  DR_MOV,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.CR_MOV>=T.CR_MOV AND H.DR_MOV>=T.DR_MOV) THEN  H.CR_MOV - isnull(T.CR_MOV,0)
     WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.CR_MOV<T.CR_MOV OR H.DR_MOV<T.DR_MOV) THEN  H.CR_MOV ELSE H.CR_MOV END  CR_MOV,
H.DR_BAL CLO_DR,
H.CR_BAL CLO_CR,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.DR_MOV_LCY>=T.DR_MOV_LCY AND H.CR_MOV_LCY>=T.CR_MOV_LCY) THEN H.DR_MOV_LCY - isnull(T.DR_MOV_LCY,0)
     WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.DR_MOV_LCY<T.DR_MOV_LCY OR H.CR_MOV_LCY<T.CR_MOV_LCY) THEN  H.DR_MOV_LCY ELSE H.DR_MOV_LCY END DR_MOV_LCY,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.CR_MOV_LCY>=T.CR_MOV_LCY AND H.DR_MOV_LCY>=T.DR_MOV_LCY) THEN  H.CR_MOV_LCY - isnull(T.CR_MOV_LCY,0)
     WHEN T.PERIOD_CODE=H.PERIOD_CODE AND (H.CR_MOV_LCY<T.CR_MOV_LCY OR H.DR_MOV_LCY<T.DR_MOV_LCY) THEN  H.CR_MOV_LCY ELSE H.CR_MOV_LCY END CR_MOV_LCY,
H.DR_BAL_LCY CLO_DR_LCY,
H.CR_BAL_LCY CLO_CR_LCY INTO #GL1
from BBCDW..FACT_DAILY_GL_HISTORY H LEFT JOIN #GL T ON H.GL_CODE=T.GL_CODE  AND H.CCY_CODE=T.CCY_CODE AND H.BRANCH_CODE=T.BRANCH_CODE  where H.BGK_DATE=@BRANCH_DATE
and H.GL_CODE not in ('32121000','32122000','32112000','32111000','32100000','13211000','13351000')
insert into #GL1
select 
H.BGK_DATE,H.BRANCH_CODE,H.GL_CODE,H.CCY_CODE,H.FIN_YEAR,H.PERIOD_CODE,H.PARENT_GL,H.LEAF,H.CATEGORY,
T.DR_BAL OPN_DR,
T.CR_BAL OPN_CR,
T.DR_BAL_LCY OPN_DR_LCY,
T.CR_BAL_LCY OPN_CR_LCY,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE THEN  H.DR_MOV - isnull(T.DR_MOV,0)
     ELSE H.DR_MOV END  DR_MOV,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE  THEN  H.CR_MOV - isnull(T.CR_MOV,0)
     ELSE H.CR_MOV END  CR_MOV,
H.DR_BAL CLO_DR,
H.CR_BAL CLO_CR,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE  THEN H.DR_MOV_LCY - isnull(T.DR_MOV_LCY,0)
      ELSE H.DR_MOV_LCY END DR_MOV_LCY,
CASE WHEN T.PERIOD_CODE=H.PERIOD_CODE  THEN  H.CR_MOV_LCY - isnull(T.CR_MOV_LCY,0)
      ELSE H.CR_MOV_LCY END CR_MOV_LCY,
H.DR_BAL_LCY CLO_DR_LCY,
H.CR_BAL_LCY CLO_CR_LCY 
from BBCDW..FACT_DAILY_GL_HISTORY H LEFT JOIN #GL T ON H.GL_CODE=T.GL_CODE  AND H.CCY_CODE=T.CCY_CODE AND H.BRANCH_CODE=T.BRANCH_CODE  where H.BGK_DATE=@BRANCH_DATE
