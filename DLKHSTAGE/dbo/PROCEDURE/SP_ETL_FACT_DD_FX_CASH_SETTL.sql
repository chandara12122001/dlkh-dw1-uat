			--SELECT 
					
			--		@FROM_DEPOSIT_FIXED =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='Y' THEN E.LCY_AMOUNT ELSE 0 END),0)

			--FROM SGTB_AC_ENTRIES E
			--INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			--WHERE 
			--	E.TRN_DT=@BRANCH_DATE AND
			--	AC.ACCOUNT_TYPE='Y'
			--AND E.AC_CCY<>'KHR'
			--SELECT 
					
			--		@FROM_DEPOSIT_FIXED_KHR =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='Y' THEN E.FCY_AMOUNT ELSE 0 END),0)

			--FROM SGTB_AC_ENTRIES E
			--INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			--WHERE 
			--	E.TRN_DT=@BRANCH_DATE AND
			--	AC.ACCOUNT_TYPE='Y'
			--AND E.AC_CCY='KHR'

			---FROM NBC---
			SELECT 
					@FROM_NBC =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' THEN E.FCY_AMOUNT/@EX_RATE_THB ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			INNER JOIN BBCDW..DIM_CUSTOMER CUS ON CUS.CUSTOMER_NO=AC.CUST_NO AND CUS.IS_CURRENT=1
			WHERE 
				CUS.CUSTOMER_CATEGORY='NBC'
			AND E.AC_CCY='THB'
			SELECT 
					@FROM_NBC =@FROM_NBC+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' THEN E.FCY_AMOUNT*@EX_RATE_EUR ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			INNER JOIN BBCDW..DIM_CUSTOMER CUS ON CUS.CUSTOMER_NO=AC.CUST_NO AND CUS.IS_CURRENT=1
			WHERE 
				CUS.CUSTOMER_CATEGORY='NBC'
			AND E.AC_CCY='EUR'
			SELECT 
					@FROM_NBC =@FROM_NBC+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' THEN E.LCY_AMOUNT ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			INNER JOIN BBCDW..DIM_CUSTOMER CUS ON CUS.CUSTOMER_NO=AC.CUST_NO AND CUS.IS_CURRENT=1
			WHERE 
				CUS.CUSTOMER_CATEGORY='NBC'
			AND E.AC_CCY='USD'

			SELECT 
					
					@FROM_NBC_KHR =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' THEN E.FCY_AMOUNT ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			INNER JOIN BBCDW..DIM_CUSTOMER CUS ON CUS.CUSTOMER_NO=AC.CUST_NO AND CUS.IS_CURRENT=1
			WHERE 
				CUS.CUSTOMER_CATEGORY='NBC'
			AND E.AC_CCY='KHR'

			-- GET SALE OF FOREIGN CURRENCIES
			SELECT
					@SALE_CURRENCY=ISNULL(SUM(CASE WHEN E.DRCR_IND='D' THEN E.FCY_AMOUNT/@EX_RATE_THB ELSE 0 END),0)
			FROM SGTB_AC_ENTRIES E
			WHERE E.AC_NO in ('11110001','11110002')
					AND E.TRN_CODE in (212,213) 
					and e.AC_CCY='THB'
					AND E.TRN_DT=@BRANCH_DATE
			SELECT
					@SALE_CURRENCY=@SALE_CURRENCY+ISNULL(SUM(CASE WHEN E.DRCR_IND='D' THEN E.FCY_AMOUNT*@EX_RATE_EUR ELSE 0 END),0)
			FROM SGTB_AC_ENTRIES E
			WHERE E.AC_NO in ('11110001','11110002')
					AND E.TRN_CODE in (212,213) 
					and e.AC_CCY='EUR'
					AND E.TRN_DT=@BRANCH_DATE

			SELECT
					@SALE_CURRENCY=@SALE_CURRENCY+ISNULL(SUM(CASE WHEN E.DRCR_IND='D' THEN E.LCY_AMOUNT ELSE 0 END),0)
			FROM SGTB_AC_ENTRIES E
			WHERE E.AC_NO in ('11110001','11110002')
					AND E.TRN_CODE in (212,213) 
					and e.AC_CCY='USD'
