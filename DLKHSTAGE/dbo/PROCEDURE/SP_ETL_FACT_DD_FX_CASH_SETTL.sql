			 CU.CCY_CODE='THB' -- Exclude KHR Currency
				AND   G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and m.IS_CURRENT=1)
				  AND G.DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)
			
			SELECT 
					@OPENING_CASH_BAL=isnull(@OPENING_CASH_BAL,0)+ isnull(SUM((G.OPN_FCY_BAL*GC.VAL)*@EX_RATE_EUR),0),
					@OPENING_CASH_BAL_AD=isnull(@OPENING_CASH_BAL_AD,0)+ isnull(SUM((G.OPN_FCY_BAL*GC.VAL)*@EX_RATE_EUR_L),0)
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			INNER JOIN BBCDW..DIM_CURRENCY CU ON CU.CCY_KEY=G.CCY_KEY
			WHERE 
			 CU.CCY_CODE='EUR' -- Exclude KHR Currency
				AND   G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and m.IS_CURRENT=1)
				  AND G.DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)
			SELECT 
					@OPENING_CASH_BAL=isnull(@OPENING_CASH_BAL,0)+ isnull(SUM(G.OPN_LCY_BAL*GC.VAL),0),
					@OPENING_CASH_BAL_AD=isnull(@OPENING_CASH_BAL_AD,0)+ isnull(SUM(G.OPN_LCY_BAL*GC.VAL),0)
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			INNER JOIN BBCDW..DIM_CURRENCY CU ON CU.CCY_KEY=G.CCY_KEY
			WHERE 
			 CU.CCY_CODE='USD' -- Exclude KHR Currency
				AND   G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and m.IS_CURRENT=1)
				  AND G.DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)

			SELECT 
					@OPENING_CASH_BAL_KHR= isnull(SUM(G.OPN_FCY_BAL*GC.VAL),0)
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			WHERE 
			 G.CCY_KEY=96 -- Include KHR Currency
				AND   G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and m.IS_CURRENT=1)
				  AND G.DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)
	

			-- GET TODAY DR AND CR CASH MOVEMENT
			SELECT 
					@TODAY_CASH_DR_BAL= ISNULL(SUM(G.DR_MOV_LCY),0),
					@TODAY_CASH_CR_BAL=ISNULL(SUM(G.CR_MOV_LCY),0)
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			INNER JOIN BBCDW..DIM_CURRENCY CU ON CU.CCY_KEY=G.CCY_KEY
			WHERE  
			CU.CCY_CODE='USD' -- Exclude KHR Currency
				AND  DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)
				 AND G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and IS_CURRENT=1)
			SELECT 
					@TODAY_CASH_DR_BAL= @TODAY_CASH_DR_BAL+ISNULL(SUM(G.DR_MOV_FCY),0)*@EX_RATE_EUR,
					@TODAY_CASH_CR_BAL=@TODAY_CASH_CR_BAL+ISNULL(SUM(G.CR_MOV_FCY),0)*@EX_RATE_EUR
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			INNER JOIN BBCDW..DIM_CURRENCY CU ON CU.CCY_KEY=G.CCY_KEY
			WHERE  
			CU.CCY_CODE='EUR' -- Exclude KHR Currency
				AND  DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)
				 AND G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and IS_CURRENT=1)
			SELECT 
					@TODAY_CASH_DR_BAL= @TODAY_CASH_DR_BAL+ISNULL(SUM(G.DR_MOV_FCY),0)/@EX_RATE_THB,
					@TODAY_CASH_CR_BAL=@TODAY_CASH_CR_BAL+ISNULL(SUM(G.CR_MOV_FCY),0)/@EX_RATE_THB
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			INNER JOIN BBCDW..DIM_CURRENCY CU ON CU.CCY_KEY=G.CCY_KEY
			WHERE  
			CU.CCY_CODE='THB' -- Exclude KHR Currency
				AND  DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)
				 AND G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and IS_CURRENT=1)
			SELECT 
					@TODAY_CASH_DR_BAL_KHR= ISNULL(SUM(G.DR_MOV_FCY),0),
					@TODAY_CASH_CR_BAL_KHR=ISNULL(SUM(G.CR_MOV_FCY),0)
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			INNER JOIN BBCDW..DIM_CURRENCY CU ON CU.CCY_KEY=G.CCY_KEY
			WHERE  
			CU.CCY_CODE='KHR' -- Exclude KHR Currency
				AND  DATE_KEY=CONVERT(VARCHAR,@BRANCH_DATE,112)
				 AND G.GL_KEY IN(SELECT GL_KEY FROM BBCDW..DIM_GLMASTER M WHERE M.GL_CODE IN('11110000') and IS_CURRENT=1)
			--*********************
			--	FROM DEPOSIT
			--*********************
select E.TRN_REF_NO,AC_NO,AC_CCY,E.TRN_CODE,LCY_AMOUNT,FCY_AMOUNT,DRCR_IND into #ENTRIES from SGTB_AC_ENTRIES E where E.TRN_DT=@BRANCH_DATE and E.TRN_CODE IN (205,206,207)
UPDATE R SET AC_CCY=E1.AC_CCY,FCY_AMOUNT=E1.FCY_AMOUNT from  #ENTRIES R inner join #ENTRIES E1 on E1.TRN_REF_NO=R.TRN_REF_NO WHERE E1.AC_NO in ('11110001','11110002')
select @PETTY_CASH=isnull(SUM(LCY_AMOUNT),0) from #ENTRIES  WHERE AC_NO in ('001000002300075',
'001000002300064',
'001000002300100',
'001000002300086',
'001000002300053',
'001000002300097') and AC_CCY='USD' and TRN_CODE<>'205'
select @PETTY_CASH=@PETTY_CASH+isnull(SUM(FCY_AMOUNT),0)*@EX_RATE_EUR from #ENTRIES  WHERE AC_NO in ('001000002300075',
'001000002300064',
'001000002300100',
'001000002300086',
'001000002300053',
'001000002300097') and AC_CCY='EUR' and TRN_CODE<>'205'
select @PETTY_CASH=@PETTY_CASH+isnull(SUM(FCY_AMOUNT),0)/@EX_RATE_THB from #ENTRIES  WHERE AC_NO in ('001000002300075',
'001000002300064',
'001000002300100',
'001000002300086',
'001000002300053',
'001000002300097') and AC_CCY='THB' and TRN_CODE<>'205'
select @PETTY_CASH_KHR=isnull(SUM(FCY_AMOUNT),0) from #ENTRIES  WHERE AC_NO in ('001000002300075',
'001000002300064',
'001000002300100',
'001000002300086',
'001000002300053',
'001000002300097') and AC_CCY='KHR' and TRN_CODE<>'205'
UPDATE R SET FCY_AMOUNT=0,LCY_AMOUNT=0 from  #ENTRIES R inner join #ENTRIES E1 on E1.TRN_REF_NO=R.TRN_REF_NO WHERE E1.AC_NO in ('001000002300075',
'001000002300064',
'001000002300100',
'001000002300086',
'001000002300053',
'001000002300097')

			-- GET FROM DEPOSIT (SAVING, CURRENT AND FIXED)
			SELECT 
					@FROM_DEPOSIT=ISNULL(SUM(CASE WHEN E.DRCR_IND='C' THEN ISNULL(E.FCY_AMOUNT,0)/@EX_RATE_THB ELSE 0 END),0) ,
					@FROM_DEPOSIT_SAVING =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='S' THEN ISNULL(E.FCY_AMOUNT,0)/@EX_RATE_THB ELSE 0 END),0),
					@FROM_DEPOSIT_CURRENT =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='U' THEN ISNULL(E.FCY_AMOUNT,0)/@EX_RATE_THB ELSE 0 END),0),
					@FROM_DEPOSIT_FIXED =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='Y' THEN ISNULL(E.FCY_AMOUNT,0)/@EX_RATE_THB ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			WHERE 
				AC.ACCOUNT_TYPE IN('S','U','Y')
			AND E.AC_CCY='THB'
			SELECT 
					@FROM_DEPOSIT=@FROM_DEPOSIT+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' THEN ISNULL(E.FCY_AMOUNT,0)*@EX_RATE_EUR ELSE 0 END),0) ,
					@FROM_DEPOSIT_SAVING =@FROM_DEPOSIT_SAVING+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='S' THEN ISNULL(E.FCY_AMOUNT,0)*@EX_RATE_EUR ELSE 0 END),0),
					@FROM_DEPOSIT_CURRENT =@FROM_DEPOSIT_CURRENT+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='U' THEN ISNULL(E.FCY_AMOUNT,0)*@EX_RATE_EUR ELSE 0 END),0),
					@FROM_DEPOSIT_FIXED =@FROM_DEPOSIT_FIXED+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='Y' THEN ISNULL(E.FCY_AMOUNT,0)*@EX_RATE_EUR ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			WHERE 
				AC.ACCOUNT_TYPE IN('S','U','Y')
			AND E.AC_CCY='EUR'
			SELECT 
					@FROM_DEPOSIT=@FROM_DEPOSIT+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' THEN ISNULL(E.LCY_AMOUNT,0) ELSE 0 END),0) ,
					@FROM_DEPOSIT_SAVING =@FROM_DEPOSIT_SAVING+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='S' THEN ISNULL(E.LCY_AMOUNT,0) ELSE 0 END),0),
					@FROM_DEPOSIT_CURRENT =@FROM_DEPOSIT_CURRENT+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='U' THEN ISNULL(E.LCY_AMOUNT,0) ELSE 0 END),0),
					@FROM_DEPOSIT_FIXED =@FROM_DEPOSIT_FIXED+ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='Y' THEN ISNULL(E.LCY_AMOUNT,0) ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			WHERE 
				AC.ACCOUNT_TYPE IN('S','U','Y')
			AND E.AC_CCY='USD'
			SELECT 
					@FROM_DEPOSIT_KHR=ISNULL(SUM(CASE WHEN E.DRCR_IND='C'  THEN E.FCY_AMOUNT ELSE 0 END),0) ,
					@FROM_DEPOSIT_SAVING_KHR =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='S' THEN E.FCY_AMOUNT ELSE 0 END),0),
					@FROM_DEPOSIT_CURRENT_KHR =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='U' THEN E.FCY_AMOUNT ELSE 0 END),0),
					@FROM_DEPOSIT_FIXED_KHR =ISNULL(SUM(CASE WHEN E.DRCR_IND='C' AND AC.ACCOUNT_TYPE='Y' THEN E.FCY_AMOUNT ELSE 0 END),0)

			FROM #ENTRIES E
			INNER JOIN  BBCDW..DIM_CUST_ACCOUNT AC ON E.AC_NO=AC.CUST_AC_NO AND AC.IS_CURRENT=1
			WHERE 
				AC.ACCOUNT_TYPE IN('S','U','Y') 
			AND E.AC_CCY='KHR'
			
