CREATE PROCEDURE [dbo].[SP_ETL_FACT_DD_FX_CASH_SETTL] 
	@BRANCH_DATE AS DATE,@DB_NAME VARCHAR(15),@SERVER_NAME VARCHAR(15),@IS_RE_PROC BIT=0
AS
BEGIN
	
	 IF OBJECT_ID('tempdb..#ENTRIES') IS NOT NULL
  DROP TABLE #ENTRIES
	IF @IS_RE_PROC=1 
	BEGIN
			DELETE FROM BBCNBCMART..FACT_DAILY_FX_CASH_SETTLEMENT WHERE DATE_KEY= CONVERT(VARCHAR,@BRANCH_DATE,112)
			DELETE FROM BBCNBCMART..FACT_WEEKLY_FX_CASH_SETTLEMENT WHERE DATE_KEY= CONVERT(VARCHAR,@BRANCH_DATE,112)
	END

	DECLARE @EX_RATE_KHR AS FLOAT=0.0,@EX_RATE_THB AS FLOAT=0.0,@EX_RATE_EUR AS FLOAT=0.0,@TODAY_DATE AS DATE;
	DECLARE @EX_RATE_KHR_L AS FLOAT,@EX_RATE_THB_L AS FLOAT,@EX_RATE_EUR_L AS FLOAT;

	DECLARE
			@OPENING_CASH_BAL_KHR		MONEY=0.0, 

			@ADJUSTMENT_Open numeric(10,2),@ADJUSTMENT_Close numeric(10,2),
			-- FROM DEPOSIT
			@TODAY_CASH_CR_BAL_KHR		MONEY=0.0,
			@FROM_DEPOSIT_KHR			MONEY=0.0,
			@FROM_DEPOSIT_SAVING_KHR	MONEY=0.0,
			@FROM_DEPOSIT_CURRENT_KHR	MONEY=0.0,
			@FROM_DEPOSIT_FIXED_KHR		MONEY=0.0,
			@FROM_NBC_KHR				MONEY=0.0,
			@SALE_CURRENCY_KHR			MONEY=0.0,
			@OTHER_CASH_RECEIPT_KHR		MONEY=0.0,

			-- WITHDRAWAL
			@TODAY_CASH_DR_BAL_KHR		MONEY=0.0,
			@WITHDRAWAL_DEPOSIT_KHR		MONEY=0.0,
			@WITHDRAWAL_DEPOSIT_SAVING_KHR MONEY=0.0,
			@WITHDRAWAL_DEPOSIT_CURRENT_KHR MONEY=0.0,
			@WITHDRAWAL_DEPOSIT_FIXED_KHR MONEY=0.0,
			@TO_NBC_KHR				MONEY=0.0,
			@PURCHASE_CURRENCY_KHR		MONEY=0.0,
			@OTHER_PAYMENTS_KHR			MONEY=0.0 ,

			@OPENING_CASH_BAL		MONEY=0.0, 
			@OPENING_CASH_BAL_AD       Money,
			-- FROM DEPOSIT
			@TODAY_CASH_CR_BAL		MONEY=0.0,
			@FROM_DEPOSIT			MONEY=0.0,
			@FROM_DEPOSIT_SAVING	MONEY=0.0,
			@FROM_DEPOSIT_CURRENT	MONEY=0.0,
			@FROM_DEPOSIT_FIXED		MONEY=0.0,
			@FROM_NBC				MONEY=0.0,
	@SALE_CURRENCY			MONEY=0.0,
			@OTHER_CASH_RECEIPT		MONEY=0.0,

			-- WITHDRAWAL
			@TODAY_CASH_DR_BAL		MONEY=0.0,
			@WITHDRAWAL_DEPOSIT		MONEY=0.0,
			@WITHDRAWAL_DEPOSIT_SAVING MONEY=0.0,
			@WITHDRAWAL_DEPOSIT_CURRENT MONEY=0.0,
			@WITHDRAWAL_DEPOSIT_FIXED MONEY=0.0,
			@TO_NBC				MONEY=0.0,
			@PURCHASE_CURRENCY		MONEY=0.0,
			@OTHER_PAYMENTS			MONEY=0.0,
			@PETTY_CASH MONEY,
			@PETTY_CASH_KHR MONEY

			
			select @EX_RATE_KHR=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='KHR' and RATE_DATE=@BRANCH_DATE and BRANCH_CODE='001'
			select @EX_RATE_THB=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='THB' and RATE_DATE=@BRANCH_DATE and BRANCH_CODE='001'
			select @EX_RATE_EUR=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='EUR' and RATE_DATE=@BRANCH_DATE and BRANCH_CODE='001'

			select @EX_RATE_KHR_L=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='KHR' and RATE_DATE=(select MAX(RATE_DATE) from BBCDW..DIM_RATE where RATE_DATE<@BRANCH_DATE) and BRANCH_CODE='001'
			select @EX_RATE_THB_L=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='THB' and RATE_DATE=(select MAX(RATE_DATE) from BBCDW..DIM_RATE where RATE_DATE<@BRANCH_DATE) and BRANCH_CODE='001'
			select @EX_RATE_EUR_L=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='EUR' and RATE_DATE=(select MAX(RATE_DATE) from BBCDW..DIM_RATE where RATE_DATE<@BRANCH_DATE) and BRANCH_CODE='001'

			IF ISNULL(@EX_RATE_KHR,0)<=0  
			BEGIN
			
			select MAX(RATE_DATE) from BBCDW..DIM_RATE where RATE_DATE<@BRANCH_DATE
			select @EX_RATE_KHR=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='KHR' and RATE_DATE=(select MAX(RATE_DATE) from BBCDW..DIM_RATE where RATE_DATE<@BRANCH_DATE) and BRANCH_CODE='001'
			select @EX_RATE_THB=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='THB' and RATE_DATE=(select MAX(RATE_DATE) from BBCDW..DIM_RATE where RATE_DATE<@BRANCH_DATE) and BRANCH_CODE='001'
			select @EX_RATE_EUR=MID_RATE from BBCDW..DIM_RATE where RATE_TYPE='NBC' and CCY1='USD' and CCY2='EUR' and RATE_DATE=(select MAX(RATE_DATE) from BBCDW..DIM_RATE where RATE_DATE<@BRANCH_DATE) and BRANCH_CODE='001'
			
			END
			Print @EX_RATE_KHR 
			Print @EX_RATE_EUR
			Print @EX_RATE_THB
			-- GET OPENING CASH BALANCE
			SELECT 
					@OPENING_CASH_BAL= isnull(SUM((G.OPN_FCY_BAL*GC.VAL)/@EX_RATE_THB),0),
					@OPENING_CASH_BAL_AD=isnull(SUM((G.OPN_FCY_BAL*GC.VAL)/@EX_RATE_THB_L),0)
			FROM BBCDW..FACT_DAILY_GL_BAL G 
			INNER JOIN BBCDW..DIM_GLCATEGORY GC ON GC.CATEGORY_KEY=G.CATEGORY_KEY
			INNER JOIN BBCDW..DIM_CURRENCY CU ON CU.CCY_KEY=G.CCY_KEY
